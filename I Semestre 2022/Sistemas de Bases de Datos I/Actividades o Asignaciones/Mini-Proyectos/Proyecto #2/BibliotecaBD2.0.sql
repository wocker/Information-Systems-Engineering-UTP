
-- Creacion de la Base de Datos 
CREATE DATABASE BibliotecaBD


-- Creacion de Tablas 
USE BibliotecaBD

-- Creacion de la Tabla Autor
CREATE TABLE Autor (
	a_id int  
		CONSTRAINT pk_Autor_a_id PRIMARY KEY,
	nombre VARCHAR (20) NOT NULL
)

-- Creacion de la Tabla Usuario
CREATE TABLE Usuario (
	u_id INT
		CONSTRAINT pk_Usuario_id_u PRIMARY KEY,
	nombre VARCHAR (20) NOT NULL,
	telefono CHAR (12)
)

-- Creacion de la Tabla Libro
CREATE TABLE Libro (
	l_id INT
		CONSTRAINT pk_Libro_l_id PRIMARY KEY, 
	titulo VARCHAR (25) NOT NULL,
	precio DECIMAL (5,2) NOT NULL
		CONSTRAINT ck_Libro_precio CHECK ((precio >= 1.00) and (precio <= 150.00)),
	formato CHAR (2)
		CONSTRAINT ck_Libro_formato CHECK (formato in ('td','tb','cd')),
	u_id INT
		CONSTRAINT fk_Libro_u_id FOREIGN KEY REFERENCES Usuario(u_id)
)

-- Creacion de la Tabla Autor_de_Libro 
CREATE TABLE Autor_de_Libro (
	a_id INT
		CONSTRAINT fk_Autor_de_Libro_a_id FOREIGN KEY REFERENCES Autor(a_id),
	l_id INT 
		CONSTRAINT fk_Autor_de_Libro_l_id FOREIGN KEY REFERENCES Libro(l_id)
		CONSTRAINT pk_Autor_de_LibrRo_a_id_l_id PRIMARY KEY (a_id, l_id) 
)


